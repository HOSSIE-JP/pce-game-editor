function makeTmxWithTilesets(sources, layerXml = null) {
  const tilesetSources = Array.isArray(sources) ? sources : Array.from(arguments);
  const layers = layerXml || [
    ' <layer id="1" name="Ground" width="1" height="1">',
    '  <data encoding="csv">1</data>',
    ' </layer>',
  ].join('\n');
  return [
    '<?xml version="1.0" encoding="UTF-8"?>',
    '<map version="1.10" orientation="orthogonal" width="1" height="1" tilewidth="8" tileheight="8" infinite="0">',
    ...tilesetSources.map((source, index) => ` <tileset firstgid="${index * 32 + 1}" source="${source}"/>`),
    layers,
    '</map>',
    '',
  ].join('\n');
}

