test('asset-checker declares a builder role', () => {
  const userData = fs.mkdtempSync(path.join(os.tmpdir(), 'md-editor-asset-checker-plugin-test-'));
  const pluginManager = loadWithMockedElectron(path.join(__dirname, '..', 'plugin-manager.js'), { userData });
  const plugin = pluginManager.listPlugins().find((item) => item.id === 'asset-checker');

  assert.ok(plugin);
  assert.equal(plugin.name, 'アセットチェッカー');
  assert.equal(plugin.roles.length, 1);
  assert.equal(plugin.roles[0].id, 'builder');
  assert.deepEqual(plugin.pluginTypes, ['build']);
});

test('asset-checker groups supported assets and filters non-BGM wav files', () => {
  const grouped = assetChecker.collectAssets(sampleAssets());

  assert.deepEqual(grouped.images.map((entry) => entry.name), ['img_title']);
  assert.deepEqual(grouped.sprites.map((entry) => entry.name), ['spr_hero']);
  assert.deepEqual(grouped.tilemaps.map((entry) => entry.name), ['map_stage', 'scroll_stage']);
  assert.deepEqual(grouped.bgms.map((entry) => entry.name), ['bgm_legacy', 'bgm_pcm', 'bgm_stage']);
  assert.equal(grouped.bgms.some((entry) => entry.name === 'se_hit'), false);
});

