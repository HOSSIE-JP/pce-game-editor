'use strict';

const fs = require('fs');
const path = require('path');

const GRID_ROWS = 20;
const GRID_COLS = 15;
const BLOCK_NAMES = [
  'BLOCK_EMPTY',
  'BLOCK_WHITE',
  'BLOCK_YELLOW',
  'BLOCK_GREEN',
  'BLOCK_BLUE',
  'BLOCK_RED',
  'BLOCK_GRAY',
];
const POWERUP_NAMES = [
  'POWERUP_NONE',
  'POWERUP_MULTI_BALL',
  'POWERUP_STRONG',
  'POWERUP_SPEED_UP',
  'POWERUP_BARRIER',
];

function normalizeGrid(source, fallbackValue, names) {
  return Array.from({ length: GRID_ROWS }, (_, row) => (
    Array.from({ length: GRID_COLS }, (_, col) => {
      const raw = Array.isArray(source?.[row]) ? source[row][col] : fallbackValue;
      const index = Number.isInteger(raw) ? raw : Number(raw || fallbackValue);
      return names[index] || names[fallbackValue] || names[0];
    })
  ));
}

function normalizePowerups(source) {
  const grid = Array.from({ length: GRID_ROWS }, () => Array(GRID_COLS).fill(0));
  if (Array.isArray(source)) return normalizeGrid(source, 0, POWERUP_NAMES);

  if (source && typeof source === 'object') {
    Object.entries(source).forEach(([key, value]) => {
      const match = /^(\d+)[,:-](\d+)$/.exec(key);
      if (!match) return;
      const row = Number(match[1]);
      const col = Number(match[2]);
      if (row >= 0 && row < GRID_ROWS && col >= 0 && col < GRID_COLS) {
        grid[row][col] = Number(value || 0);
      }
    });
  }
  return normalizeGrid(grid, 0, POWERUP_NAMES);
}

function cGridRows(grid) {
  return grid
    .map((row) => `    { ${row.join(', ')} }`)
    .join(',\n');
}

function findFirstImageAsset(assets = []) {
  const image = assets.find((asset) => String(asset?.type || '').toUpperCase() === 'IMAGE');
  return image?.name || '';
}

function imagePointer(symbol) {
  const value = String(symbol || '').trim();
  return value ? `&${value}` : 'NULL';
}

function makeDefaultStage(assets) {
  const blocks = Array.from({ length: GRID_ROWS }, (_, row) => (
    Array.from({ length: GRID_COLS }, (_, col) => (
      row === 5 && col >= 2 && col <= 12 ? 1 : 0
    ))
  ));
  return {
    name: 'Stage 1',
    order: 1,
    blocks,
    power_ups: {},
    clear_image_symbol: findFirstImageAsset(assets),
  };
}

function exportStagesHeader(projectDir, stages = [], settings = {}, assets = []) {
  const stageList = stages.length > 0 ? stages : [makeDefaultStage(assets)];
  const outPath = path.join(projectDir, 'inc', 'stages.h');
  const fallbackImage = settings.clear_image_symbol || findFirstImageAsset(assets);

  const blockDefs = [];
  const powerupDefs = [];
  const tableRows = [];

  stageList.forEach((stage, index) => {
    const stageNo = index + 1;
    const blockGrid = normalizeGrid(stage.blocks, 0, BLOCK_NAMES);
    const powerupGrid = normalizePowerups(stage.powerups || stage.power_ups);
    const clearImage = stage.clear_image_symbol || stage.background_image_symbol || fallbackImage;

    blockDefs.push(`/* Stage ${stageNo}: ${stage.name || `Stage ${stageNo}`} */\nstatic const u8 stage_${stageNo}_blocks[GRID_ROWS][GRID_COLS] = {\n${cGridRows(blockGrid)}\n};`);
    powerupDefs.push(`static const u8 stage_${stageNo}_powerups[GRID_ROWS][GRID_COLS] = {\n${cGridRows(powerupGrid)}\n};`);
    tableRows.push(`    { stage_${stageNo}_blocks, stage_${stageNo}_powerups, ${imagePointer(clearImage)} }`);
  });

  const header = `/* Generated by block-stage-exporter */\n#ifndef _STAGES_H_\n#define _STAGES_H_\n\n#include \"game.h\"\n#include \"game_resources.h\"\n\n#define STAGE_COUNT ${stageList.length}\n\n${blockDefs.join('\n\n')}\n\n${powerupDefs.join('\n\n')}\n\nstatic const StageInfo stage_table[STAGE_COUNT] = {\n${tableRows.join(',\n')}\n};\n\n#endif /* _STAGES_H_ */\n`;

  fs.mkdirSync(path.dirname(outPath), { recursive: true });
  fs.writeFileSync(outPath, header, 'utf-8');
  return { headerPath: outPath };
}

module.exports = {
  exportStagesHeader,
};
